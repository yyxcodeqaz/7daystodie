package com.daystodie.entity;


public class WhiteList {


    private long whiteListId;
    private String whiteListName;
    private long whiteListOnline;
    private String whiteListNameDesc;
    private String whiteListNameAuthor;
    private String whiteListNameVersion;


    public long getWhiteListId() {
        return whiteListId;
    }

    public void setWhiteListId(long whiteListId) {
        this.whiteListId = whiteListId;
    }


    public String getWhiteListName() {
        return whiteListName;
    }

    public void setWhiteListName(String whiteListName) {
        this.whiteListName = whiteListName;
    }


    public long getWhiteListOnline() {
        return whiteListOnline;
    }

    public void setWhiteListOnline(long whiteListOnline) {
        this.whiteListOnline = whiteListOnline;
    }


    public String getWhiteListNameDesc() {
        return whiteListNameDesc;
    }

    public void setWhiteListNameDesc(String whiteListNameDesc) {
        this.whiteListNameDesc = whiteListNameDesc;
    }


    public String getWhiteListNameAuthor() {
        return whiteListNameAuthor;
    }

    public void setWhiteListNameAuthor(String whiteListNameAuthor) {
        this.whiteListNameAuthor = whiteListNameAuthor;
    }


    public String getWhiteListNameVersion() {
        return whiteListNameVersion;
    }

    public void setWhiteListNameVersion(String whiteListNameVersion) {
        this.whiteListNameVersion = whiteListNameVersion;
    }

}
